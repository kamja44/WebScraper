def plus(a=0, b=0):
    print(a+b)


def minus(a=0, b=0):
    print(a-b)


def multiplication(a=1, b=1):
    print(a*b)


def division(a=1, b=1):
    print(a/b)


def square(a=1, b=1):
    print(a**b)


print("plus")
plus(5, 10)
plus(5)
print("minus")
minus(5, 10)
minus(5)
print("multiplication")
multiplication(5, 10)
multiplication(5)
print("division")
division(5, 10)
division(5)
print("square")
square(5, 10)
square(5)
